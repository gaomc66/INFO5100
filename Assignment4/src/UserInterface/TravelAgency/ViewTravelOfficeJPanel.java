    private void populateCustomerTable(){
        DefaultTableModel dfm = (DefaultTableModel)customerjTable.getModel();
        dfm.setRowCount(0);
        travelOffice.getCustomerDirectory().getCustomerList().forEach((Customer customer)->{
            Object[] row = new Object[dfm.getColumnCount()];
            row[0] = customer;
            row[1] = customer.getId();
            row[2] = customer.getIdentityType();
            row[3] = customer.getIdentityID();
            
            dfm.addRow(row);
        });
    }
    
    private void populateTicketTable(){
        DefaultTableModel dfm = (DefaultTableModel)ticketsjTable.getModel();
        dfm.setRowCount(0);
        travelOffice.getTicketDirectory().getTicketList().forEach((Ticket ticket)->{
            System.out.println("Tickets:::::::");
            Object[] row = new Object[dfm.getColumnCount()];
            row[0] = ticket;
            row[1] = ticket.getCustomerInfo();
            row[2] = ticket.getFlightNumber();
            row[3] = ticket.getAirplaneInfo();
            row[4] = ticket.getSeatInfo();
            row[5] = ticket.isPending();
            
            dfm.addRow(row);
        });
    }
    
