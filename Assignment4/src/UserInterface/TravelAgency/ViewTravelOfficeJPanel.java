    private void savejButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_savejButtonActionPerformed
        // TODO add your handling code here:
        String name = namejTextField.getText();
        if(name.equals(null)||name.startsWith(" ")){
            JOptionPane.showMessageDialog(null, "Please do not input name start with space");
            this.modifyjButton.setEnabled(true);
            this.namejTextField.setEditable(false);
            this.savejButton.setEnabled(false);
            this.namejTextField.setText(travelOffice.getName());
        }else if(name.equals(travelOffice.getName())){
            JOptionPane.showMessageDialog(null, "It is the same as before, nothing modified");
            this.modifyjButton.setEnabled(true);
            this.namejTextField.setEditable(false);
            this.savejButton.setEnabled(false);
            this.namejTextField.setText(travelOffice.getName());
        }else{
            JOptionPane.showMessageDialog(null, "Modified Successfully");
            travelOffice.setName(name);
            this.modifyjButton.setEnabled(true);
            this.namejTextField.setEditable(false);
            this.savejButton.setEnabled(false);
            this.namejTextField.setText(travelOffice.getName());
        }
    }//GEN-LAST:event_savejButtonActionPerformed

    private void confirmTicketjButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_confirmTicketjButtonActionPerformed
        // TODO add your handling code here:
        int selectedRow = this.ticketsjTable.getSelectedRow();
        if(selectedRow <0){
            JOptionPane.showMessageDialog(null, "Please select a row");
        }else{
            Ticket ticket = (Ticket)this.ticketsjTable.getValueAt(selectedRow, 0);
            JOptionPane.showMessageDialog(null, "Success generated a ticket, ticket into:\n"
                    + "ticket number:" + ticket.getTicketNumber() +"\n"
                    + "customer information" + ticket.getCustomerInfo() +"\n"
                    + "flight number" + ticket.getFlightInfo() + "\n"
                    + "seat information" + ticket.getSeatInfo() + "\n"
                    + "\n"
                    + "Order generated and send to the Airliner" + ticket.getAirlinerName()
            );
            ticket.setPending(false);
            populateTicketTable();
            
            ticket.getAirliner().addOrderedTicket(ticket);
            
        }
        
    }//GEN-LAST:event_confirmTicketjButtonActionPerformed


