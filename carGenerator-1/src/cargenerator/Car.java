package cargenerator;

/**
 *
 * @author gaomc
 */
public class Car {

    private String brand;
    private String model;
    private String drivetrains;
    private String engines;
    private String style;
    private String exteriorColor;
    private String interiorColor;
    private String mspr;
    private String fuelType;
    private String mpg;
    private String mileage;
    private String transmission;
    private String serialNum;
    private String dimension;
    private String carFaxNum;
    private String imgPath;

    public String getImgPath() {
        return imgPath;
    }

    public void setImgPath(String imgPath) {
        this.imgPath = imgPath;
    }

    public String getBrand() {
        return brand;
    }

    public void setBrand(String brand) {
        this.brand = brand;
    }

    public String getModel() {
        return model;
    }

    public void setModel(String model) {
        this.model = model;
    }

    public String getDrivetrains() {
        return drivetrains;
    }

    public void setDrivetrains(String drivetrains) {
        this.drivetrains = drivetrains;
    }

    public String getEngines() {
        return engines;
    }

    public void setEngines(String engines) {
        this.engines = engines;
    }

    public String getStyle() {
        return style;
    }

    public void setStyle(String style) {
        this.style = style;
    }

    public String getExteriorColor() {
        return exteriorColor;
    }

    public void setExteriorColor(String exteriorColor) {
        this.exteriorColor = exteriorColor;
    }

    public String getInteriorColor() {
        return interiorColor;
    }

    public void setInteriorColor(String interiorColor) {
        this.interiorColor = interiorColor;
    }

    public String getMspr() {
        return mspr;
    }

    public void setMspr(String mspr) {
        this.mspr = mspr;
    }

    public String getFuelType() {
        return fuelType;
    }

    public void setFuelType(String fuelType) {
        this.fuelType = fuelType;
    }

    public String getMpg() {
        return mpg;
    }

    public void setMpg(String mpg) {
        this.mpg = mpg;
    }

    public String getMileage() {
        return mileage;
    }

    public void setMileage(String mileage) {
        this.mileage = mileage;
    }

    public String getTransmission() {
        return transmission;
    }

    public void setTransmission(String transmission) {
        this.transmission = transmission;
    }

    public String getSerialNum() {
        return serialNum;
    }

    public void setSerialNum(String serialNum) {
        this.serialNum = serialNum;
    }

    public String getDimension() {
        return dimension;
    }

    public void setDimension(String dimension) {
        this.dimension = dimension;
    }

    public String getCarFaxNum() {
        return carFaxNum;
    }

    public void setCarFaxNum(String carFaxNum) {
        this.carFaxNum = carFaxNum;
    }
    
}
