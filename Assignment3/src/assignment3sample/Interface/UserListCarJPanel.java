    private void viewBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_viewBtnActionPerformed
        // TODO add your handling code here:
        int selectedRow = carListTable.getSelectedRow();
        if (selectedRow < 0){
            JOptionPane.showMessageDialog(null, "Please select a Row!");
            return;
        }else{
            Car car = (Car) carListTable.getValueAt(selectedRow, 0);
            UserViewSingleCarJPanel userViewSingleCarJPanel = new UserViewSingleCarJPanel(viewJPanel, car);
            viewJPanel.add(userViewSingleCarJPanel);
            CardLayout layout = (CardLayout) viewJPanel.getLayout();
            layout.next(viewJPanel);
        }
    }//GEN-LAST:event_viewBtnActionPerformed


