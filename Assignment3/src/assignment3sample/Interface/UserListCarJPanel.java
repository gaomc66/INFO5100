    private void populate(List<Car> searchResult){
        
        DefaultTableModel dtm = (DefaultTableModel)carListTable.getModel();
        dtm.setRowCount(0);
        
        searchResult.stream().map((car) -> {
            Object[] row = new Object[dtm.getColumnCount()];
            row[0] = car;
            row[1] = car.getAvailble_city();
            row[2] = car.getMin_seats();
            row[3] = car.getMax_seats();
            row[4] = car.getManufactured_year();
            row[5] = car.getSerial_num();
            row[6] = car.isMaintenance_certificate();
            row[7] = car.isAvailable();
            row[8] = car.getModel_num();
            return row;
        }).forEachOrdered((row) -> {
            dtm.addRow(row);
        });
        
        if(searchResult.size()==0){
            JOptionPane.showMessageDialog(null, "Cannot find a car like this!");
        }
    }

